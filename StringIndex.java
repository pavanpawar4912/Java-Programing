class StringIndex
{
public static void main(String args[])
{
int index;
String a = "Welcome To Java";
index = a.indexOf('w');
System.out.println("index :"+index);
index = a.indexOf('e');
System.out.println("index:"+index);
index = a.indexOf('J');
System.out.println("index: " +index);
}
}
